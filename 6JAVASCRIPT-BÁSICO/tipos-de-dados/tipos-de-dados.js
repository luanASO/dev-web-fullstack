
// Tipos de dados:
    // Boolean - tem dois valores true ou false, 1 ou 0, sim ou não.
    //Null - o valor null é a certeza que não há nada dentro da variável.
    //Undefined - este valor não dá pra ter certeza se não tem nada dentro ou se tem algo.
    //Number - número, podendo ser quebrados ou inteiros.
    //String - é uma cadeia de caracteres, palavras, frases, espaços. O que defini uma string são aspas ('') ou (""). \n - indica para pular um linha.

var a = "Palavra"; // atribui a "a" uma cadeia de caracteres (string).
var b = 1; // atribui a "b" um número (number).
var c = null; // atribui a "c" nenhum valor (null).
var d = undefined; // atribui a "d" um valor indefinido (undefined)
var e = true; false; // recebe true ou false (verdadeiro ou falso).

console.log(a);
console.log(b);
console.log(c)
console.log(d)
console.log(e)




console.log("1" + "1") // concatena uma string na outra.
console.log(1 + "1") // transforma o número 1 em uma string e concatena com a outra.
console.log(1 + 1) // soma um número o numero com outro número.