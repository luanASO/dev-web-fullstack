// Operadores aritiméticos
    // + Adição 
    // - Subtração
    // * Multiplicação
    // / Divisão
    // % Módulo
    // ** Exponênciação
    // ++ Incremento
    // -- Decremento


var a = 10; // atribui o valor 10 a variável "a".
var b = 15; // atribui o valor 15 a variável "b".

var c = a + b; // atribui a "c" o valor de "a" + "b".
console.log(c);

var d = a - b ; // atribui a "c" o valor de "a" - "b".
console.log(d);

var c = a * b; // atribui a "c" o valor de "a" * "b".
console.log(c);

var d = a / b; // atribui a "c" o valor de "a" / "b".
console.log(d);

var e = a % b // atribui a "c" o valor de "a" % "b".
console.log(e);

var f = a ** 2; // atribui a "c" o valor de "a" ** "b";
console.log(f);

var g = 30;
g = g + 1; // atribui a "a" o seu valor mais 1.
g = g - 1; // atribui a "a" o seu valor menos 1.
console.log(g);


var h = 20;
var i = 25;
h++; // atribui a "a" o seu valor mais 1.
i--; // atribui a "a" o seu valor menos 1.
console.log(h)
console.log(i)

var j = 4;
var k = j++; // atribui a "k", o valor "j" mais 1. Mas primeiro vai atribuir o valor a "j", e depois vai incrementar.
console.log(k);
var k = ++j; // atribui a "k" o valor de "j" maiu um, por que agora ele incrementa a "j" mais 1, e depois atribui a "k".
console.log(k);
