// "window" é um objeto mais importante no JS, pois, com ele que podemos manipular dados do navegador ("html, css, etc"). 

// podemos utilizar "propriedades" e "métodos" do objeto "window", mas não precisamos usar window (ponto) é depois a propriedade ou método.

window.console.log(""); // "console" é uma propriedade do objeto "window"
console.log(""); // mas não precisamos utilizar o "window", para utilizar o "console".

window.alert("") // um método do objeto "window".
alert("") // mas não precisa utilizar o "window", para utilizar o "alert()".




